__global__ void __classifyCubes(RegularGrid3Accessor<float> f,
                                MemoryBlock3Accessor<unsigned char> cube_type,
                                MemoryBlock3Accessor<unsigned int> count,
                                float isovalue) {
  int i = blockIdx.x * blockDim.x + threadIdx.x;
  int j = blockIdx.y * blockDim.y + threadIdx.y;
  int k = blockIdx.z * blockDim.z + threadIdx.z;
  if (f.isIndexStored(i + 1, j + 1, k + 1)) {
    unsigned char type = 0;
    if (f(i, j, k) < isovalue)
      type |= 1;
    if (f(i + 1, j, k) < isovalue)
      type |= 2;
    if (f(i + 1, j, k + 1) < isovalue)
      type |= 4;
    if (f(i, j, k + 1) < isovalue)
      type |= 8;
    if (f(i, j + 1, k) < isovalue)
      type |= 16;
    if (f(i + 1, j + 1, k) < isovalue)
      type |= 32;
    if (f(i + 1, j + 1, k + 1) < isovalue)
      type |= 64;
    if (f(i, j + 1, k + 1) < isovalue)
      type |= 128;
    cube_type(i, j, k) = type;
    size_t c = 0;
    for (size_t t = 0; t < 5; t++)
      if (tri_table[type][t * 3] >= 0)
        c++;
    count(i, j, k) = c;
  }
}

__device__ point3f intersect(float value, point3f p1, point3f p2, float v1,
                             float v2) {
  if (p2 < p1) {
    point3f temp;
    temp = p1;
    p1 = p2;
    p2 = temp;
    float vt = v1;
    v1 = v2;
    v2 = vt;
  }
  point3f p;
  if (fabs(v1 - v2) > 0.00001)
    p = p1 + (p2 - p1) / (v2 - v1) * (value - v1);
  else
    p = p1;
  return p;
}

