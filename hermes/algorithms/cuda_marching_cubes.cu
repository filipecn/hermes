      vertlist[2] = intersect(isovalue, p[2], p[3], val[2], val[3]);
    if (edge_table[cubeindex] & 8)
      vertlist[3] = intersect(isovalue, p[3], p[0], val[3], val[0]);
    if (edge_table[cubeindex] & 16)
      vertlist[4] = intersect(isovalue, p[4], p[5], val[4], val[5]);
    if (edge_table[cubeindex] & 32)
      vertlist[5] = intersect(isovalue, p[5], p[6], val[5], val[6]);
    if (edge_table[cubeindex] & 64)
      vertlist[6] = intersect(isovalue, p[6], p[7], val[6], val[7]);
    if (edge_table[cubeindex] & 128)
      vertlist[7] = intersect(isovalue, p[7], p[4], val[7], val[4]);
    if (edge_table[cubeindex] & 256)
      vertlist[8] = intersect(isovalue, p[0], p[4], val[0], val[4]);
    if (edge_table[cubeindex] & 512)
      vertlist[9] = intersect(isovalue, p[1], p[5], val[1], val[5]);
    if (edge_table[cubeindex] & 1024)
      vertlist[10] = intersect(isovalue, p[2], p[6], val[2], val[6]);
    if (edge_table[cubeindex] & 2048)
      vertlist[11] = intersect(isovalue, p[3], p[7], val[3], val[7]);
    for (size_t t = 0; tri_table[cubeindex][t * 3] != -1; ++t)
      for (size_t v = 0; v < 3; v++) {
        indices[index_offset(i, j, k) * 3 + t * 3 + v] =
            index_offset(i, j, k) * 3 + t * 3 + v;
        for (size_t c = 0; c < 3; c++)
          vertices[(index_offset(i, j, k) + t) * 3 * 3 + v * 3 + c] =
              vertlist[tri_table[cubeindex][t * 3 + v]][c];
      }
  }
}

__global__ void __generateNormals(CuMemoryBlock1Accessor<float> vertices,
                                  CuMemoryBlock1Accessor<float> normals) {

  int tid = blockIdx.x * blockDim.x + threadIdx.x;
  if (tid < vertices.size() / 9) {
    vec3f v[3];
    for (int i = 0; i < 3; ++i) {
      v[i].x = vertices[tid * 9 + i * 3 + 0];
      v[i].y = vertices[tid * 9 + i * 3 + 1];
      v[i].z = vertices[tid * 9 + i * 3 + 2];
    }
    vec3f n = cross(v[1] - v[0], v[2] - v[0]);
    if (n.length2() != 0)
      n = normalize(n);
    // else
    //   n.x = 1.f;
    for (int i = 0; i < 3; i++) {
      normals[tid * 9 + i * 3 + 0] = n.x;
      normals[tid * 9 + i * 3 + 1] = n.y;
      normals[tid * 9 + i * 3 + 2] = n.z;
    }
  }
}

template <>
void MarchingCubes::extractSurface(RegularGrid3Df &f, CuMemoryBlock1f &vertices,
                                   CuMemoryBlock1u &indices, float isovalue,
                                   CuMemoryBlock1f *normals) {
  // classify cubes
  vec3u size = f.resolution() - vec3u(1);
  MemoryBlock3Duc cube_type(size);
  cube_type.allocate();
  MemoryBlock3Du d_index_offset(size);
  d_index_offset.allocate();
  ThreadArrayDistributionInfo td(size);
  __classifyCubes<<<td.gridSize, td.blockSize>>>(
      f.accessor(), cube_type.accessor(), d_index_offset.accessor(), isovalue);
  // compute indices
  MemoryBlock3Hu h_index_offset(size);
  h_index_offset.allocate();
  memcpy(h_index_offset, d_index_offset);
  size_t count = 0;
  for (auto e : h_index_offset.accessor()) {
    int tmp = e.value;
    e.value = count;
    count += tmp;
  }
  if (!count)
    return;
  memcpy(d_index_offset, h_index_offset);
  // compute vertices
  vertices.resize(count * 3 * 3);
  indices.resize(count * 3);
  __generateVertices<<<td.gridSize, td.blockSize>>>(
      vertices.accessor(), indices.accessor(), f.accessor(),
      d_index_offset.accessor(), cube_type.accessor(), isovalue);
  if (normals) {
    normals->resize(count * 3 * 3);
    ThreadArrayDistributionInfo ntd(count);
    __generateNormals<<<ntd.gridSize, ntd.blockSize>>>(vertices.accessor(),
                                                       normals->accessor());
  }
}

